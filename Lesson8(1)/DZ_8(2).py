# Программа с функционалом калькулятора для операций с двумя числами
def calculator():
    # Бесконечный цикл для выполнения нескольких операций
    while True:
        try:
            # Ввод двух чисел и операции от пользователя
            num1 = float(input("Введите первое число: "))
            operator = input("Введите операцию (+, -, *, /): ")
            num2 = float(input("Введите второе число: "))
            result = 0
            # Выполнение соответствующей операции
            if operator == '+':
                result = num1 + num2
            elif operator == '-':
                result = num1 - num2
            elif operator == '*':
                result = num1 * num2
            elif operator == '/':
                if num2 == 0:
                    # Обработка исключения деления на ноль
                    print("Ошибка: Деление на ноль невозможно!")
                    continue # Продолжить следующую итерацию цикла
                result = num1 / num2
            else:
                # Обработка неверного оператора
                print("Ошибка: Неверный оператор!")
                continue
            # Вывод результата
            print(f"Результат: {result}")
        except ValueError:
            # Обработка исключения, если пользователь ввел не число
            print("Ошибка: Введено неверное значение. Пожалуйста, введите число.")
        except Exception as e:
            # Обработка других возможных исключений
            print(f"Произошла непредвиденная ошибка: {e}")
        # Запрос на продолжение работы
        next_calculation = input("Хотите выполнить еще одно вычисление? (да/нет): ")
        if next_calculation.lower() != 'да':
            break # Выход из цикла, если ответ не "да"
# Запуск калькулятора
# **Ответ:**
calculator()